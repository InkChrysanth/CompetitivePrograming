template <typename T>
void read(T &x)
{
	x=0; char ch=getchar(); ll f=1;
	while(!isdigit(ch))
	{
		if(ch=='-') f*=-1;
		ch=getchar();
	}
	while(isdigit(ch))
	{
		x=x*10+(ch^48);
		ch=getchar();
	}
	x*=f;
}
template <typename T, typename...Args>
void read(T &first, Args &...args)
{
	read(first);
	read(args...);
}

signed main()
{
	int n, sum=0; read(n);
	for(int i=1; i<=n; i++)
	{
		int x;
		read(x);
		sum+=x;
	}
	cout<<sum<<endl;
	return 0;
}
